package co.md.bean;

public class CampaignResponse {

	private long id;
	private String name;
	
	public CampaignResponse() {
		super();
	}
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
